package Entidades;

public class Dados {

    private String nome;
    private double preco;

    public Dados(String nome, double preco) {
        this.nome = nome;
        this.preco = preco;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getIdade() {
        return preco;
    }
    public void setIdade(double idade) {
        this.preco = idade;
    }
}
